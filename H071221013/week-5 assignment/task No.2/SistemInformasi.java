class SistemInformasi {
    String nama;
    int umur;
    String gender;

    public void setNama(String nama) {
        this.nama = nama;
    }
    public String getNama() {
        return nama;
    }

    public void setUmur(int umur) {
        this.umur = umur;
    }
    public int getUmur() {
        return umur;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }
    public String getGender() {
        return gender;
    }
}